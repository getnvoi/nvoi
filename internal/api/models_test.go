func TestCommandLog_Lifecycle(t *testing.T) {
	db := TestDB()

	user := User{GithubUsername: "test", GithubToken: "ghp_test"}
	db.Create(&user)
	ws := Workspace{Name: "default", CreatedBy: user.ID}
	db.Create(&ws)
	db.Create(&WorkspaceUser{UserID: user.ID, WorkspaceID: ws.ID, Role: "owner"})
	repo := Repo{WorkspaceID: ws.ID, Name: "app"}
	db.Create(&repo)

	cl := CommandLog{
		RepoID:     repo.ID,
		UserID:     user.ID,
		Kind:       "instance.set",
		Name:       "master",
		Status:     "succeeded",
		DurationMs: 4500,
	}
	if err := db.Create(&cl).Error; err != nil {
		t.Fatalf("create command log: %v", err)
	}
	if cl.ID == "" {
		t.Error("command log ID should be set")
	}

	var loaded CommandLog
	db.First(&loaded, "id = ?", cl.ID)
	if loaded.Kind != "instance.set" {
		t.Errorf("kind = %q, want instance.set", loaded.Kind)
	}
	if loaded.DurationMs != 4500 {
		t.Errorf("duration = %d, want 4500", loaded.DurationMs)
	}
}
