func TestRepo_SSHKeysAutoGenerated(t *testing.T) {
	db := TestDB()

	user := User{GithubUsername: "test", GithubToken: "ghp_test"}
	db.Create(&user)
	ws := Workspace{Name: "default", CreatedBy: user.ID}
	db.Create(&ws)
	db.Create(&WorkspaceUser{UserID: user.ID, WorkspaceID: ws.ID, Role: "owner"})

	repo := Repo{WorkspaceID: ws.ID, Name: "app"}
	if err := db.Create(&repo).Error; err != nil {
		t.Fatalf("create repo: %v", err)
	}

	// Reload to trigger AfterFind (decrypt).
	var loaded Repo
	db.First(&loaded, "id = ?", repo.ID)

	if loaded.SSHPrivateKey == "" {
		t.Error("SSHPrivateKey should be auto-generated, got empty")
	}
	if loaded.SSHPublicKey == "" {
		t.Error("SSHPublicKey should be auto-generated, got empty")
	}
	if loaded.SSHPrivateKey[:5] != "-----" {
		t.Errorf("SSHPrivateKey should be PEM, got %q...", loaded.SSHPrivateKey[:20])
	}
	if loaded.SSHPublicKey[:4] != "ssh-" {
		t.Errorf("SSHPublicKey should start with ssh-, got %q...", loaded.SSHPublicKey[:20])
	}
}

