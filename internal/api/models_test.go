func TestRepo_AgentTokenAutoGenerated(t *testing.T) {
	db := TestDB()

	user := User{GithubUsername: "test-agent", GithubToken: "ghp_test"}
	db.Create(&user)
	ws := Workspace{Name: "default", CreatedBy: user.ID}
	db.Create(&ws)

	repo := Repo{WorkspaceID: ws.ID, Name: "app"}
	if err := db.Create(&repo).Error; err != nil {
		t.Fatalf("create repo: %v", err)
	}

	if repo.AgentToken == "" {
		t.Error("AgentToken should be auto-generated at creation")
	}
	if len(repo.AgentToken) != 64 { // 32 bytes hex-encoded
		t.Errorf("AgentToken length = %d, want 64 hex chars", len(repo.AgentToken))
	}
	if repo.AgentTokenHash == "" {
		t.Error("AgentTokenHash should be set at creation")
	}
}

