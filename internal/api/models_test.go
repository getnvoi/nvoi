func TestRepo_AgentTokenValidatesViaHash(t *testing.T) {
	db := TestDB()

	user := User{GithubUsername: "test-validate", GithubToken: "ghp_test"}
	db.Create(&user)
	ws := Workspace{Name: "default", CreatedBy: user.ID}
	db.Create(&ws)

	repo := Repo{WorkspaceID: ws.ID, Name: "myapp", Environment: "prod"}
	db.Create(&repo)

	// The plaintext token validates against the stored hash.
	if !ValidateAgentToken(repo.AgentToken, repo.AgentTokenHash) {
		t.Error("freshly created repo token should validate against its own hash")
	}

	// A different token does not validate.
	if ValidateAgentToken("wrong-token", repo.AgentTokenHash) {
		t.Error("wrong token should not validate")
	}
}

